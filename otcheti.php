                </p>
                <p>
                    Уже через три дня на сайте зарегистрировались 40 человек волонтеров, а через месяц больше 100. Работа закипела в буквальном 
                    смысле слова. Мужчины разгружали машины с гуманитарной помощью от губернатора Московской области Андрея Воробьева и различных 
                    благотворительных организаций, женщины собирали продуктовые наборы, волонтеры у кого были свои машины развозили продуктовые 
                    наборы тем, кто обратился за помощью. 
                </p>
                <p>
                    Нуждающихся в помощи людей было много: многодетные и малообеспеченные семьи, пенсионеры, ветераны, люди, которые 
                    остались без средств на существование из-за увольнения в разгар пандемии. 
                </p>
                <p>
                    Волонтеры старались помогать всем, кто нуждался в помощи. Очень быстро новость о работе добровольцев разлетелась по всему городу. 
                    Не все люди могли присоединиться к команде, но многие хотели помогать. Предлагали деньги, чтобы поддержать работу добровольцев. 
                </p>
                <p>
