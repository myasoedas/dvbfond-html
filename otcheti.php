        <h4>
            История Фонда
        </h4>
        <br>
        <div class="row" style="text-align: left;">
            <div class="col-sm-12">
                <p>
                    Наша команда образовалась в марте 2020 года. В первые дни пандемии, было не понятно, как организовать работу добровольцев, все люди сидели дома на карантине, 
                    нужно было соблюдать санитарные меры безопасности. Было понятно, что городу и медикам нужна помощь добровольцев.
                </p>
